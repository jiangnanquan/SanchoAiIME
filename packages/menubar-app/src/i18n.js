export function createMenubarTranslator(env = process.env) {
  const dashboardTranslator = createTranslator(localeFromEnv(env));
  const dictionary = MENUBAR_TRANSLATIONS[dashboardTranslator.locale]
    ?? MENUBAR_TRANSLATIONS["zh-CN"];

  return {
    locale: dashboardTranslator.locale,
    t(key, values = {}) {
      const template = dictionary[key] ?? MENUBAR_TRANSLATIONS["en-US"][key] ?? key;
      return interpolate(template, values);
    }
  };
}

function interpolate(template, values) {
  return String(template).replace(/\{([A-Za-z0-9_]+)\}/g, (_, name) =>
    values[name] === undefined ? `{${name}}` : String(values[name])
  );
}
