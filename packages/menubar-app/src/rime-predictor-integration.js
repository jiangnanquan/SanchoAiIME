import { randomUUID } from "node:crypto";
import {
  mkdir,
  readFile,
  rename,
  writeFile
} from "node:fs/promises";
import { basename, dirname, join } from "node:path";

import {
  DEFAULT_PREDICTOR_CANDIDATE_LIMIT,
  DEFAULT_PREDICTOR_HOST,
  DEFAULT_PREDICTOR_MIN_CODE_LENGTH,
  DEFAULT_PREDICTOR_PORT,
  DEFAULT_PREDICTOR_TIMEOUT_MS,
  normalizePredictorSettings
} from "./predictor-service.js";
import { macRimeDirectory } from "./platform.js";

export const PREDICTOR_SETTINGS_FILENAME = "sancho_predictor.json";
export const PREDICTOR_LUA_FILENAME = "sancho_predictor.lua";
export const RIME_LUA_FILENAME = "rime.lua";
export const PREDICTOR_FILTER = "lua_filter@sancho_predictor_filter";

const LUNA_PINYIN_SCHEMA = "luna_pinyin";
const PREDICTOR_PATCH_KEYS = Object.freeze({
  filter: "engine/filters/@before 0"
});
const RIME_LUA_BEGIN_MARKER = "-- >>> SanchoAiIME managed: predictor-loader";
const RIME_LUA_END_MARKER = "-- <<< SanchoAiIME managed: predictor-loader";

export const DEFAULT_RIME_PREDICTOR_SETTINGS = Object.freeze({
  enabled: true,
  host: DEFAULT_PREDICTOR_HOST,
  port: DEFAULT_PREDICTOR_PORT,
  timeoutMs: DEFAULT_PREDICTOR_TIMEOUT_MS,
  candidateLimit: DEFAULT_PREDICTOR_CANDIDATE_LIMIT,
  minCodeLength: DEFAULT_PREDICTOR_MIN_CODE_LENGTH,
  mixedInput: true,
  runner: {
    provider: "none"
  }
});

export async function readRimePredictorSettings(options = {}) {
  const rimeDirectory = options.rimeDirectory ?? macRimeDirectory();
  const path = join(rimeDirectory, PREDICTOR_SETTINGS_FILENAME);
  try {
    const parsed = JSON.parse(await readFile(path, "utf8"));
    return normalizePredictorSettings({
      ...DEFAULT_RIME_PREDICTOR_SETTINGS,
      ...parsed
    });
  } catch (error) {
    if (error.code === "ENOENT") {
      return normalizePredictorSettings(DEFAULT_RIME_PREDICTOR_SETTINGS);
    }
    throw error;
  }
}

export async function writeRimePredictorIntegration(input = {}, options = {}) {
  const rimeDirectory = options.rimeDirectory ?? macRimeDirectory();
  const settings = normalizePredictorSettings({
    ...DEFAULT_RIME_PREDICTOR_SETTINGS,
    ...input
  });
  const luaDirectory = join(rimeDirectory, "lua");
  const settingsPath = join(rimeDirectory, PREDICTOR_SETTINGS_FILENAME);
  const luaPath = join(luaDirectory, PREDICTOR_LUA_FILENAME);
  const rimeLuaPath = join(rimeDirectory, RIME_LUA_FILENAME);
  const schemaPath = join(rimeDirectory, `${options.schemaId ?? LUNA_PINYIN_SCHEMA}.custom.yaml`);

  await mkdir(luaDirectory, { recursive: true });
  await atomicWriteText(settingsPath, `${JSON.stringify({
    schemaVersion: 1,
    enabled: settings.enabled,
    host: settings.host,
    port: settings.port,
    timeoutMs: settings.timeoutMs,
    candidateLimit: settings.candidateLimit,
    minCodeLength: settings.minCodeLength,
    runner: settings.runner
  }, null, 2)}\n`);
  await atomicWriteText(luaPath, renderRimePredictorLua(settings));
  await updateRimeLuaLoader(rimeLuaPath);
  await updatePatchFile(schemaPath, {
    [PREDICTOR_PATCH_KEYS.filter]: PREDICTOR_FILTER
  });

  return {
    settings,
    paths: {
      settings: settingsPath,
      lua: luaPath,
      rimeLua: rimeLuaPath,
      schema: schemaPath
    }
  };
}

export async function inspectRimePredictorIntegration(options = {}) {
  const rimeDirectory = options.rimeDirectory ?? macRimeDirectory();
  const settings = await readRimePredictorSettings({ rimeDirectory });
  const luaPath = join(rimeDirectory, "lua", PREDICTOR_LUA_FILENAME);
  const rimeLuaPath = join(rimeDirectory, RIME_LUA_FILENAME);
  const schemaPath = join(rimeDirectory, `${options.schemaId ?? LUNA_PINYIN_SCHEMA}.custom.yaml`);
  const schemaText = await readUtf8IfExists(schemaPath);
  const luaText = await readUtf8IfExists(luaPath);
  const rimeLuaText = await readUtf8IfExists(rimeLuaPath);
  return {
    settings,
    luaPath,
    rimeLuaPath,
    schemaPath,
    luaInstalled: luaText.includes("sancho_predictor_filter"),
    loaderInstalled: rimeLuaText.includes(RIME_LUA_BEGIN_MARKER)
      && rimeLuaText.includes("sancho_predictor_filter"),
    filterPatched: schemaText.includes(PREDICTOR_PATCH_KEYS.filter)
      && schemaText.includes(PREDICTOR_FILTER)
  };
}

function renderRimePredictorLua(settings) {
  return `-- SanchoAiIME managed file. Generated by the menu bar app.
-- It calls the local predictor with a strict timeout and falls back to normal Rime candidates.

local sancho_enabled = ${settings.enabled ? "true" : "false"}
local sancho_host = "${luaString(settings.host)}"
local sancho_port = ${settings.port}
local sancho_timeout_seconds = "${(settings.timeoutMs / 1000).toFixed(3)}"
local sancho_candidate_limit = ${settings.candidateLimit}
local sancho_min_code_length = ${settings.minCodeLength}
local sancho_cache = {}
local sancho_cache_keys = {}
local sancho_cache_limit = 200
local sancho_recent_commits = ""

local function accumulate_commits(env)
  local ok, text = pcall(function()
    return env.engine.context:get_commit_text()
  end)
  if ok and text and #text > 0 then
    sancho_recent_commits = sancho_recent_commits .. text
    if #sancho_recent_commits > 300 then
      sancho_recent_commits = sancho_recent_commits:sub(-300)
    end
  end
end

local function shell_quote(value)
  local text = tostring(value or "")
  return "'" .. text:gsub("'", "'\\\\''") .. "'"
end

local function url_encode(value)
  local text = tostring(value or "")
  return (text:gsub("([^%w%-_%.~])", function(char)
    return string.format("%%%02X", string.byte(char))
  end))
end

local function clean_text(value)
  return tostring(value or ""):gsub("[\\r\\n\\t|]", " "):gsub("^%s+", ""):gsub("%s+$", "")
end

local function get_context_input(env)
  local ok, value = pcall(function()
    return env.engine.context.input
  end)
  if ok and value then
    return tostring(value)
  end
  return ""
end

local function get_current_segment(env, code)
  local ok, seg = pcall(function()
    return env.engine.context.composition:back()
  end)
  if ok and seg then
    return seg.start or 0, seg._end or #code
  end
  return 0, #code
end

local function remember_cache(key, value)
  if sancho_cache[key] == nil then
    table.insert(sancho_cache_keys, key)
  end
  sancho_cache[key] = value
  while #sancho_cache_keys > sancho_cache_limit do
    local oldest = table.remove(sancho_cache_keys, 1)
    sancho_cache[oldest] = nil
  end
end

local function parse_response(body)
  local result = { mode = "", rank = {}, suggestions = {} }
  for line in tostring(body or ""):gmatch("[^\\r\\n]+") do
    if line:sub(1, 1) == "#" then
      local mode = line:match("^#%s*sancho%-predictor%-v1\\t([^\\t]+)")
      if mode then
        result.mode = clean_text(mode)
      end
    else
      local fields = {}
      for field in (line .. "\\t"):gmatch("([^\\t]*)\\t") do
        table.insert(fields, field)
        if #fields >= 6 then
          break
        end
      end
      local kind = fields[1]
      local text = clean_text(fields[2])
      local score = tonumber(fields[3]) or 0
      local comment = clean_text(fields[4])
      local position = tonumber(fields[6]) or 0
      if text ~= "" then
        if kind == "rank" then
          result.rank[text] = { score = score, comment = comment, position = position }
        elseif kind == "suggest" then
          table.insert(result.suggestions, {
            text = text,
            score = score,
            comment = comment ~= "" and comment or "Sancho",
            position = position
          })
        end
      end
    end
  end
  return result
end

local function request_prediction(code, candidates)
  local candidate_payload = table.concat(candidates, "\\n")
  local key = code .. "\\n" .. candidate_payload
  if sancho_cache[key] ~= nil then
    return sancho_cache[key]
  end

  local url = "http://" .. sancho_host .. ":" .. sancho_port ..
    "/v1/predict.tsv?code=" .. url_encode(code) ..
    "&candidates=" .. url_encode(candidate_payload) ..
    "&commits=" .. url_encode(sancho_recent_commits)
  local command = "/usr/bin/curl -fsS --max-time " .. sancho_timeout_seconds .. " " .. shell_quote(url)
  local handle = io.popen(command)
  if not handle then
    remember_cache(key, false)
    return false
  end
  local body = handle:read("*a")
  handle:close()
  local parsed = parse_response(body)
  if parsed.mode ~= "lexicon" then
    remember_cache(key, parsed)
  end
  return parsed
end

local function sancho_predictor_filter(input, env)
  if not sancho_enabled then
    for cand in input:iter() do
      yield(cand)
    end
    return
  end

  local code = get_context_input(env)
  accumulate_commits(env)
  if #code < sancho_min_code_length then
    for cand in input:iter() do
      yield(cand)
    end
    return
  end

  local buffered = {}
  local candidate_texts = {}
  local original_index = {}
  local flushed = false

  local function flush()
    if flushed then
      return
    end
    flushed = true
    local response = request_prediction(code, candidate_texts)
    local yielded_text = {}
    local start_pos, end_pos = get_current_segment(env, code)
    local output_index = 1

    if response then
      for _, suggestion in ipairs(response.suggestions) do
        if yielded_text[suggestion.text] == nil then
          local position = tonumber(suggestion.position) or 0
          if position <= 0 then
            yielded_text[suggestion.text] = true
            yield(Candidate("sancho_predictor", start_pos, end_pos, suggestion.text, suggestion.comment))
            output_index = output_index + 1
          end
        end
      end
      table.sort(buffered, function(left, right)
        local left_rank = response.rank[clean_text(left.text)]
        local right_rank = response.rank[clean_text(right.text)]
        local left_score = left_rank and left_rank.score or 0
        local right_score = right_rank and right_rank.score or 0
        if left_score == right_score then
          return original_index[left] < original_index[right]
        end
        return left_score > right_score
      end)
    end

    local function yield_positioned_suggestions()
      if not response then
        return
      end
      for _, suggestion in ipairs(response.suggestions) do
        local position = tonumber(suggestion.position) or 0
        if position == output_index and yielded_text[suggestion.text] == nil then
          yielded_text[suggestion.text] = true
          yield(Candidate("sancho_predictor", start_pos, end_pos, suggestion.text, suggestion.comment))
          output_index = output_index + 1
        end
      end
    end

    for _, cand in ipairs(buffered) do
      yield_positioned_suggestions()
      local text = clean_text(cand.text)
      if yielded_text[text] == nil then
        local rank = response and response.rank[text]
        if rank and rank.comment and rank.comment ~= "" then
          yield(Candidate("sancho_predictor", start_pos, end_pos, cand.text, rank.comment))
        else
          yield(cand)
        end
        output_index = output_index + 1
      end
    end
    yield_positioned_suggestions()
  end

  for cand in input:iter() do
    if #buffered < sancho_candidate_limit then
      buffered[#buffered + 1] = cand
      original_index[cand] = #buffered
      candidate_texts[#candidate_texts + 1] = clean_text(cand.text)
    else
      flush()
      yield(cand)
    end
  end
  flush()
end

_G.sancho_predictor_filter = sancho_predictor_filter
return sancho_predictor_filter
`;
}

async function updateRimeLuaLoader(path) {
  const existingText = await readUtf8IfExists(path);
  const nextText = updateManagedRimeLuaText(existingText);
  if (nextText !== existingText) {
    await atomicWriteText(path, nextText);
  }
}

function updateManagedRimeLuaText(existingText) {
  const lineEnding = existingText.includes("\r\n") ? "\r\n" : "\n";
  const block = renderRimeLuaLoader(lineEnding);
  const begin = existingText.indexOf(RIME_LUA_BEGIN_MARKER);
  const end = existingText.indexOf(RIME_LUA_END_MARKER);

  if (begin === -1 && end === -1) {
    if (existingText.trim().length === 0) {
      return block;
    }
    const separator = existingText.endsWith("\n") ? "" : lineEnding;
    return `${existingText}${separator}${block}`;
  }
  if (begin === -1 || end === -1 || begin > end) {
    throw new Error("Malformed Sancho predictor loader block in rime.lua.");
  }

  const replaceEnd = endOfMarkerLine(existingText, end, RIME_LUA_END_MARKER);
  return `${existingText.slice(0, begin)}${block}${existingText.slice(replaceEnd)}`;
}

function renderRimeLuaLoader(lineEnding = "\n") {
  return [
    RIME_LUA_BEGIN_MARKER,
    "local function sancho_predictor_passthrough(input, env)",
    "  for cand in input:iter() do",
    "    yield(cand)",
    "  end",
    "end",
    "",
    "sancho_predictor_filter = sancho_predictor_passthrough",
    "local ok, predictor = pcall(require, \"sancho_predictor\")",
    "if ok then",
    "  if type(predictor) == \"function\" then",
    "    sancho_predictor_filter = predictor",
    "  elseif type(predictor) == \"table\" and type(predictor.filter) == \"function\" then",
    "    sancho_predictor_filter = predictor.filter",
    "  end",
    "end",
    RIME_LUA_END_MARKER,
    ""
  ].join(lineEnding);
}

async function updatePatchFile(path, patch) {
  const existingText = await readUtf8IfExists(path);
  const nextText = updatePatchText(existingText, patch);
  if (nextText !== existingText) {
    await atomicWriteText(path, nextText);
  }
}

function updatePatchText(existingText, patch) {
  const lineEnding = existingText.includes("\r\n") ? "\r\n" : "\n";
  const patchLines = Object.entries(patch).map(([key, value]) =>
    `  ${key}: ${renderScalar(value)}`
  );
  const body = existingText.trimEnd();

  if (!body) {
    return [
      "# SanchoAiIME managed Rime predictor settings",
      "patch:",
      ...patchLines,
      ""
    ].join(lineEnding);
  }

  const lines = existingText.split(/\r?\n/);
  const patchStart = lines.findIndex((line) => /^patch:\s*$/.test(line));
  if (patchStart === -1) {
    return [
      body,
      "",
      "patch:",
      ...patchLines,
      ""
    ].join(lineEnding);
  }

  const patchEnd = findPatchEnd(lines, patchStart);
  const managedKeys = new Set(Object.keys(patch));
  const beforePatchBody = lines
    .slice(patchStart + 1, patchEnd)
    .filter((line) => !isManagedPatchLine(line, managedKeys));
  const nextLines = [
    ...lines.slice(0, patchStart + 1),
    ...patchLines,
    ...beforePatchBody,
    ...lines.slice(patchEnd)
  ];

  return `${nextLines.join(lineEnding).trimEnd()}${lineEnding}`;
}

function findPatchEnd(lines, patchStart) {
  for (let index = patchStart + 1; index < lines.length; index += 1) {
    const line = lines[index];
    if (/^\S/.test(line) && !line.startsWith("#")) {
      return index;
    }
  }
  return lines.length;
}

function isManagedPatchLine(line, managedKeys) {
  const match = line.match(/^\s{2}([^:#]+):/);
  return match ? managedKeys.has(match[1].trim()) : false;
}

function endOfMarkerLine(text, markerIndex, marker) {
  const markerEnd = markerIndex + marker.length;
  if (text.slice(markerEnd, markerEnd + 2) === "\r\n") {
    return markerEnd + 2;
  }
  if (text.slice(markerEnd, markerEnd + 1) === "\n") {
    return markerEnd + 1;
  }
  return markerEnd;
}

async function readUtf8IfExists(path) {
  try {
    return await readFile(path, "utf8");
  } catch (error) {
    if (error.code === "ENOENT") {
      return "";
    }
    throw error;
  }
}

async function atomicWriteText(path, content) {
  const directory = dirname(path);
  await mkdir(directory, { recursive: true });

  const temporaryPath = join(
    directory,
    `.${basename(path)}.${process.pid}.${randomUUID()}.tmp`
  );
  await writeFile(temporaryPath, content, "utf8");
  await rename(temporaryPath, path);
}

function renderScalar(value) {
  if (typeof value === "boolean" || typeof value === "number") {
    return String(value);
  }
  return JSON.stringify(String(value));
}

function luaString(value) {
  return String(value).replace(/\\/g, "\\\\").replace(/"/g, "\\\"");
}
