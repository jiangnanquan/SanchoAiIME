export function normalizeLocale(locale) {
  if (!locale) {
    return DEFAULT_LOCALE;
  }
  const normalized = String(locale).replace("_", "-").toLowerCase();
  if (normalized === "zh" || normalized === "zh-cn" || normalized === "zh-hans") {
    return "zh-CN";
  }
  if (normalized === "en" || normalized === "en-us") {
    return "en-US";
  }
  return DEFAULT_LOCALE;
}

export function localeFromEnv(env = process.env) {
  return normalizeLocale(env.SANCHO_LOCALE);
}

export function createTranslator(locale = DEFAULT_LOCALE) {
  const resolvedLocale = normalizeLocale(locale);
  const dictionary = TRANSLATIONS[resolvedLocale] ?? TRANSLATIONS[DEFAULT_LOCALE];

  return {
    locale: resolvedLocale,
    t(key, values = {}) {
      const template = dictionary[key] ?? TRANSLATIONS["en-US"][key] ?? key;
      return interpolate(template, values);
    }
  };
}

function interpolate(template, values) {
  return String(template).replace(/\{([A-Za-z0-9_]+)\}/g, (_, name) =>
    values[name] === undefined ? `{${name}}` : String(values[name])
  );
}
